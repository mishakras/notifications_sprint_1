from fastapi import status

# Общее описание API
API_DESCRIPTION = """
## Сервис рекомендаций онлайн-кинотеатра

Этот сервис предоставляет персонализированные рекомендации фильмов
на основе истории просмотров пользователя и предпочтений.

### Основные возможности:
- 🎬 Персонализированные рекомендации фильмов
- 📊 Учет истории просмотров и завершенности видео
- 🔍 Поиск похожих фильмов по различным критериям
- ⚡ Кэширование результатов для повышения производительности
- 📈 Умное взвешивание предпочтений пользователя

### Алгоритм рекомендаций:
- Учитывает фильмы, просмотренные более чем на 50%
- Взвешивает предпочтения по режиссерам, актерам, сценаристам и жанрам
- Исключает уже просмотренные фильмы из рекомендаций
- Использует комбинированный скоринг на основе рейтинга IMDB

### Технологии:
- Elasticsearch для полнотекстового поиска
- MongoDB для хранения истории просмотров
- Redis для кэширования результатов
- Асинхронная архитектура для высокой производительности
"""

